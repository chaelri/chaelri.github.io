// ===========================================================================
// aircon_ir_codes.h — captured IR timings for Charlie's TAC-09CSA/KEI remote.
// ---------------------------------------------------------------------------
// Why raw timings instead of the IRTcl112Ac library encoder:
//   When we sniffed the real remote with a TSOP4838, every button (POWER ON,
//   POWER OFF, TEMP ±, SWING) produced two bursts per press:
//     * Frame 1 — IDENTICAL across all buttons (a "remote handshake" packet
//                 the library decodes as TCL112AC `23 CB 26 02 00 40 00 00
//                 00 00 00 00 00 65`).
//     * Frame 2 — UNIQUE per button. UNKNOWN to the library — the TAC-09CSA/
//                 KEI variant layers a proprietary payload on top of the
//                 standard TCL112AC preamble.
//   Library encoding only produces frame 1, which the aircon ignores without
//   the matching frame 2. So we replay both bursts verbatim with IRsend's
//   sendRaw(). All five arrays below come from one clean TSOP capture pass.
//
// Format: alternating mark/space durations in microseconds, starting with a
// mark, ending with a mark, modulated at 38 kHz by IRsend::sendRaw().
//
// Inter-burst gap between frame 1 and frame 2 is held in firmware as
// kInterBurstGapMs (see aircon.ino). The TSOP buffer split during capture
// confirmed the gap is >35 ms; 50 ms is the safe replay value.
// ===========================================================================

